package ex03operator;

public class E07BitOperatorPractice {

	public static void main(String[] args) {
		
		int num1 = 5, num2 = 3, num3 = -1;
		
		System.out.println(num1 & num2);
		System.out.println(num1 | num2);
		System.out.println(num1 ^ num2);
		System.out.println(~num3);
		
		
		
		

	}

}
